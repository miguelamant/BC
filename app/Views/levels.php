    <h1 style="text-align: center">Ways to implement a second hand system</h1>
    <hr class="thick-blue">
    <div style="display: flex; flex-direction:row; justify-content: center"> <a href="#"><div style="font-weight: bold;font-size: 3rem;" class="level">Important factors</div></a>
    </div>
    <p style="text-align: justify">There are several factors/concepts which affect the amount of books that can be sold second hand, and how much work the Cudi's have to put into it. <br>
        We will discuss them here and later we will refer to those factors. </p>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Hand-in period</h3>
            <p style="text-align: justify"> The period in which students can hand in their books. The longer this period, the more accessible it is for students, and thus the more books can be collected. </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-clock-o fa-5x" style="color:#426e70"></i>
        </div>

    </div>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Ease of buying</h3>
            <p style="text-align: justify"> You need to make sure the system you implement facilitates the buying of second hand books. If students have to contact and meet up with several persons in order to buy books, they probably won't do it, because of all the hassle.
                <br> Make it easy for the students by having one place where they can buy most of the books they need, and help them in knowing which books they need. </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-shopping-cart fa-5x" style="color:#426e70"></i>
        </div>
    <hr class="hori">
    </div>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Communication</h3>
            <p style="text-align: justify"> Communicating about where to hand in and to buy second hand books is of major importance. Therefore you need to reach out to students using different media for communication. Possibilities are social media, banners on campus, mails, asking for professors to spread the message etc.
                <br> Doing this in a proper way can be hard during the first week of the semester. That's why we recommend to collect books the semester before. Without this good communication it will be hard to resell a lot of books second hand.   </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-bullhorn fa-5x" style="color:#426e70"></i>
        </div>

    </div>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Price flexibility </h3>
            <p style="text-align: justify"> Some books are as good as new, while others might be in a bad state. It is convenient if the seller can determine a price for the book him/herself. This way you maintain the natural trade rules between buyers and sellers, and the Cudi's themselves are no intermediary. Compared to a system where you incorporate the second hand books in your website, you will have to set fixed prices for books which is less convenient, since not all books are in the same condition.  </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-handshake-o fa-5x" style="color:#426e70"></i>
        </div>

    </div>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Filtering required </h3>
            <p style="text-align: justify"> When working with a site-incorporated system, the organisation hosting the site has to filter/approve books. Books which are in a bad state or have words written in it should be filtered out. Doing this requires extra working hours and gives the organisation a certain responsibility for approving or disapproving books. </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-check-circle-o fa-5x" style="color:#426e70"></i>
        </div>

    </div>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Storage </h3>
            <p style="text-align: justify"> In most systems with the potential of reselling a lot of books second hand, books need to be stored between two semesters. As Cudi's have limited space, this can be an issue for some student associations. But it could be overcome by cooperating with KUL.</p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-archive fa-5x" style="color:#426e70"></i>
        </div>

    </div>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Transactions </h3>
            <p style="text-align: justify">  If you incorporate the second hand books in your site, without buying them from the students, this means you will have to pay out all students once the books are sold. For now, we did not find a bank to automate these transactions, meaning it needs to be done manually, which is not really feasible on a large scale.  </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-credit-card fa-5x" style="color:#426e70"></i>
        </div>

    </div>

    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Commission </h3>
            <p style="text-align: justify">  If the organisation is a financial intermediary, it means that you can ask for a commission when reselling the books, which is not possible in all other systems.   </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-euro fa-5x" style="color:#426e70"></i>
        </div>

    </div>

    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Financial risk </h3>
            <p style="text-align: justify">  When using the most advanced system, the organisation buys the books from the students. In order to do so, you need a large capital and you take some risks. If, for example, you buy books which suddenly are not used anymore, you made an investment without return. To compensate for those unavoidable losses, you need a decent commission.   </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-balance-scale fa-5x" style="color:#426e70"></i>
        </div>

    </div>
    <div class="centerDiv factor-box">
        <div class="twelve column ">
            <h3> Confidence </h3>
            <p style="text-align: justify"> The main problem about second hand is the confidence in the system. If, like in most previous tryouts, the amount of available books is rather limited, less people will come. Simply because the chance of finding the book you need is smaller. This is a self reinforcing process; the less books available, the less buyers will come, the less likely sellers are to resell their books, and thus even less sellers will come. That’s the challenging part of starting a second hand book system. It requires confidence from the students in the system. They need to believe that most of the books they hand in will be sold, and that the book they’re looking for is available. That’s why you need to invest enough time in communicating about the new systems. After a few successful semesters, confidence will reinforce itself in a good way, allowing the system to work at a large scale.     </p>
        </div>
        <div style="margin-left:5rem; margin-right:5rem">
            <i class="fa fa-group fa-5x" style="color:#426e70"></i>
        </div>

    </div>
